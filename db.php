<?php
// Функция для получения подключения к базе данных MySQL
function getDatabaseConnection() {
    // Создание нового подключения к базе данных с использованием MySQLi
    // Параметры: хост, имя пользователя, пароль, имя базы данных
    $conn = new mysqli('localhost', 'username', 'password', 'user_database');

    // Проверка на наличие ошибок при подключении
    if ($conn->connect_error) {
        // Если подключение не удалось, выводим ошибку и завершает выполнение скрипта
        die("Ошибка подключения: " . $conn->connect_error);
    }

    // Возвращаем объект подключения, если все в порядке
    return $conn;
}
